// Function to create a calendar month
function createMonthCalendar(monthData, monthIndex) {
  const monthDiv = document.createElement("div");
  monthDiv.className = "calendar-month bg-white p-4 rounded-lg shadow";

  const monthHeader = document.createElement("h3");
  monthHeader.className = "text-xl font-semibold mb-4";
  monthHeader.textContent = monthData.name;
  monthDiv.appendChild(monthHeader);

  const daysGrid = document.createElement("div");
  daysGrid.className = "grid grid-cols-7 gap-2";

  // Add weekday headers
  const weekdays = ["Dom", "Seg", "Ter", "Qua", "Qui", "Sex", "Sáb"];
  weekdays.forEach((day) => {
    const dayHeader = document.createElement("div");
    dayHeader.className = "text-center font-semibold text-gray-600";
    dayHeader.textContent = day;
    daysGrid.appendChild(dayHeader);
  });

  // Descobrir o primeiro dia da semana do mês
  const firstDate = new Date(2025, monthIndex, 1);
  const firstWeekDay = firstDate.getDay(); // 0 = domingo, 1 = segunda, ...

  // Adicionar células vazias antes do dia 1
  for (let i = 0; i < firstWeekDay; i++) {
    const emptyDiv = document.createElement("div");
    emptyDiv.className = "min-h-[40px]";
    daysGrid.appendChild(emptyDiv);
  }

  // Add days
  monthData.days.forEach((day) => {
    const dayDiv = document.createElement("div");
    dayDiv.className = "border p-2 min-h-[40px] text-center";

    const date = new Date(2025, monthIndex, day.date);
    const weekDay = date.getDay();
    // Sábados e domingos de todos os meses recebem #ff0000
    if (weekDay === 0 || weekDay === 6) {
      dayDiv.style.backgroundColor = "#ff0000";
      dayDiv.style.color = "#fff";
    }
    // Para janeiro, dias úteis continuam com #ff7878
    else if (monthData.name === "Janeiro") {
      dayDiv.style.backgroundColor = "#ff7878";
    }
    // Para fevereiro, dias 3,4,5,6,7 recebem #FFD700
    else if (
      monthData.name === "Fevereiro" &&
      [3, 4, 5, 6, 7].includes(day.date)
    ) {
      dayDiv.style.backgroundColor = "#FFD700";
    }

    const dateSpan = document.createElement("span");
    dateSpan.className = "font-semibold";
    dateSpan.textContent = day.date;
    dayDiv.appendChild(dateSpan);

    daysGrid.appendChild(dayDiv);
  });

  monthDiv.appendChild(daysGrid);
  return monthDiv;
}

// Initialize calendar
function initializeCalendar() {
  const container = document.getElementById("calendarContainer");

  // Create calendar for each month
  calendarData.months.forEach((month, idx) => {
    const monthCalendar = createMonthCalendar(month, idx);
    container.appendChild(monthCalendar);
  });
}

// Run when the page loads
document.addEventListener("DOMContentLoaded", initializeCalendar);
